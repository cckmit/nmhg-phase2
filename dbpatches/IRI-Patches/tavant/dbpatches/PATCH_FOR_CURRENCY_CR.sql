CREATE TABLE POLICY_FEES
   (	ID NUMBER(19,0) PRIMARY KEY, 
	AMOUNT NUMBER(19,2), 
	CURRENCY VARCHAR2(255 CHAR), 
	POLICY NUMBER(19,0), 
	IS_TRANSFERABLE NUMBER(1,0), 
	CONSTRAINT POLICY_FEES_FK FOREIGN KEY (POLICY) REFERENCES  POLICY_DEFINITION(ID)
    ) 
/
CREATE SEQUENCE POLICY_FEES_SEQ
START WITH 1000
INCREMENT BY 1
MAXVALUE 999999999999999999999999999
MINVALUE 1000
NOCYCLE
CACHE 20
NOORDER
/
