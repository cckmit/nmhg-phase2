CREATE TABLE INVENTORY_ITEM_OPTIONS
(
    inventory_item NUMBER(19,0),
    options NUMBER(19,0),
    CONSTRAINT INV_OPT_FK FOREIGN KEY (inventory_item) REFERENCES INVENTORY_ITEM (ID) ENABLE,
    CONSTRAINT INV_ITM_OPT_FK FOREIGN KEY (options) REFERENCES OPTION_INFO (ID) ENABLE
)
