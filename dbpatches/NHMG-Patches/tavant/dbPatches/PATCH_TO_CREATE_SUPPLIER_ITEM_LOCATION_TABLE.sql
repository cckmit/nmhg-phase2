CREATE TABLE SUPPLIER_ITEM_LOCATION
(
  ID      NUMBER(19,0)  NOT NULL ENABLE,
  CONSTRAINT SUPPLIER_ITEM_LOCATION_ID_PK PRIMARY KEY (ID) ENABLE,
  ITEM NUMBER(19,0) NOT NULL ENABLE,
  LOCATION_CODE    VARCHAR2(255 CHAR) NOT NULL ENABLE,
  CONSTRAINT SPR_LC_ITM_FK FOREIGN KEY (ITEM) REFERENCES item (ID) ENABLE
)
/


CREATE SEQUENCE SUPPLIER_ITEM_LOCATION_SEQ MINVALUE 1 MAXVALUE 999999999999999999999999999 INCREMENT BY 20 START WITH 110000000000440 CACHE 20 NOORDER NOCYCLE
