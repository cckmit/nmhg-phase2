ALTER TABLE ROLE ADD ROLE_TYPE  VARCHAR2(50)
/
UPDATE ROLE SET ROLE_TYPE='INTERNAL' WHERE D_INTERNAL_COMMENTS LIKE '%Internal%'
/
UPDATE ROLE SET ROLE_TYPE='APPLICATION' WHERE D_INTERNAL_COMMENTS LIKE '%Application%'
/
UPDATE ROLE SET ROLE_TYPE='EXTERNAL' WHERE ROLE_TYPE IS NULL
/
COMMIT